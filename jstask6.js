// 1__ Traffic light system 
var light="Blue";
if (light=="Red"){
    console.log("Stop")
}
else if (light=="Yellow"){
    console.log("Get ready to move")
}
else if (light=="Green"){
    console.log("Go")                
}
else{
    console.log("Invalid input")
}
// Output:-- Invalid input


